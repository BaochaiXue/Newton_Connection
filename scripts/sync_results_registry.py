#!/usr/bin/env python3
from __future__ import annotations

import json
from pathlib import Path
from typing import Any


ROOT = Path(__file__).resolve().parents[1]
REGISTRY_ROOT = ROOT / "results_meta"
TASKS_DIR = REGISTRY_ROOT / "tasks"
INDEX_MD = REGISTRY_ROOT / "INDEX.md"
LATEST_MD = REGISTRY_ROOT / "LATEST.md"


REQUIRED_TOP = {
    "registry_version",
    "task_slug",
    "task_chain",
    "bundle_root",
    "authoritative_run",
    "superseded_runs",
    "local_only_surfaces",
    "updated_at",
}

REQUIRED_CHAIN = {"task_page", "spec", "plan", "implement", "status"}
REQUIRED_RUN = {
    "run_id",
    "authoritative",
    "task_state",
    "claim_boundary",
    "local_artifact_root",
    "command_pointer",
    "expected_artifacts",
    "validator_outputs",
    "verdict_pointers",
    "repo_friendly_evidence",
    "why_passed",
    "predecessor",
    "superseded_by",
}


def _load_entry(path: Path) -> dict[str, Any]:
    obj = json.loads(path.read_text(encoding="utf-8"))
    missing = REQUIRED_TOP - obj.keys()
    if missing:
        raise ValueError(f"{path}: missing top-level keys: {sorted(missing)}")
    chain = obj.get("task_chain") or {}
    chain_missing = REQUIRED_CHAIN - chain.keys()
    if chain_missing:
        raise ValueError(f"{path}: missing task_chain keys: {sorted(chain_missing)}")
    run = obj.get("authoritative_run") or {}
    run_missing = REQUIRED_RUN - run.keys()
    if run_missing:
        raise ValueError(f"{path}: missing authoritative_run keys: {sorted(run_missing)}")
    return obj


def _task_chain_line(chain: dict[str, str]) -> str:
    return " -> ".join(
        [
            f"`{chain['task_page']}`",
            f"`{chain['spec']}`",
            f"`{chain['plan']}`",
            f"`{chain['implement']}`",
            f"`{chain['status']}`",
        ]
    )


def _write_index(entries: list[dict[str, Any]]) -> None:
    lines: list[str] = [
        "# Results Registry Index",
        "",
        "> status: generated",
        "> canonical_replacement: `python scripts/sync_results_registry.py`",
        "> owner_surface: `results_registry`",
        "> last_reviewed: `2026-04-01`",
        "> notes: Generated committed index over `results_meta/tasks/*.json`. Regenerate instead of hand-editing.",
        "",
        "Generated by `python scripts/sync_results_registry.py`.",
        "",
        "| Task | State | Authoritative | Current Run | Bundle Root | Updated |",
        "| --- | --- | --- | --- | --- | --- |",
    ]
    for entry in sorted(entries, key=lambda x: x["task_slug"]):
        run = entry["authoritative_run"]
        lines.append(
            "| "
            + " | ".join(
                [
                    f"`{entry['task_slug']}`",
                    f"`{run['task_state']}`",
                    "`yes`" if bool(run["authoritative"]) else "`no`",
                    f"`{run['run_id']}`",
                    f"`{entry['bundle_root']}`",
                    f"`{entry['updated_at']}`",
                ]
            )
            + " |"
        )
    lines.extend(
        [
            "",
            "## Task Chains",
            "",
        ]
    )
    for entry in sorted(entries, key=lambda x: x["task_slug"]):
        lines.append(f"- `{entry['task_slug']}`:")
        lines.append(f"  - {_task_chain_line(entry['task_chain'])}")
        lines.append(f"  - local bundle root: `{entry['bundle_root']}`")
    INDEX_MD.write_text("\n".join(lines).rstrip() + "\n", encoding="utf-8")


def _write_latest(entries: list[dict[str, Any]]) -> None:
    sorted_entries = sorted(entries, key=lambda x: x["updated_at"], reverse=True)
    lines: list[str] = [
        "# Latest Results Registry View",
        "",
        "> status: generated",
        "> canonical_replacement: `python scripts/sync_results_registry.py`",
        "> owner_surface: `results_registry`",
        "> last_reviewed: `2026-04-01`",
        "> notes: Generated latest-view over `results_meta/tasks/*.json`. Regenerate instead of hand-editing.",
        "",
        "Generated by `python scripts/sync_results_registry.py`.",
        "",
    ]
    for entry in sorted_entries:
        run = entry["authoritative_run"]
        lines.extend(
            [
                f"## `{entry['task_slug']}`",
                "",
                f"- updated: `{entry['updated_at']}`",
                f"- task state: `{run['task_state']}`",
                f"- authoritative: `{'yes' if bool(run['authoritative']) else 'no'}`",
                f"- current run: `{run['run_id']}`",
                f"- local artifact root: `{run['local_artifact_root']}`",
                f"- claim boundary: {run['claim_boundary']}",
                "",
            ]
        )
        why_passed = run.get("why_passed") or []
        if why_passed:
            lines.append("- why this surface matters:")
            for item in why_passed:
                lines.append(f"  - {item}")
            lines.append("")
    LATEST_MD.write_text("\n".join(lines).rstrip() + "\n", encoding="utf-8")


def main() -> int:
    entries = [_load_entry(path) for path in sorted(TASKS_DIR.glob("*.json"))]
    _write_index(entries)
    _write_latest(entries)
    print(f"Wrote {INDEX_MD.relative_to(ROOT)}")
    print(f"Wrote {LATEST_MD.relative_to(ROOT)}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
